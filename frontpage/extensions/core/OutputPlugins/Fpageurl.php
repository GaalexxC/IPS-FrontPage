/**
 * Template Plugin - Content: Block
 */
class _Fpageurl
{
	/**
	 * @brief	Can be used when compiling CSS
	 */
	public static $canBeUsedInCss = FALSE;
	
	/**
	 * Run the plug-in
	 *
	 * @param	string 		$data	  The initial data from the tag
	 * @param	array		$options    Array of options
	 * @return	string		Code to eval
	 */
	public static function runPlugin( $data, $options )
	{
		if ( \is_numeric( $data ) )
		{
			try
			{
				$url = \IPS\frontpage\Fpages\Fpage::load( $data )->url();
			}
			catch( \OutOfRageException $ex )
			{
				$url = NULL;
			}
		}
		else
		{
			$data = ltrim( $data );
			$url = \IPS\Http\Url::internal( 'app=frontpage&module=fpages&controller=fpage&path=' . $data, 'front', 'content_fpage_path', array( $data ) );
		}
		
		return "'" . (string) $url . "'";
	}
}