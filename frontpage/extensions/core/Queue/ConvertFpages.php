/**
 * Background Task: Convert bbcode fpages to html fpages post-3.4 upgrade
 */
class _ConvertFpages
{
	/**
	 * Run Background Task
	 *
	 * @param	mixed						$data	Data as it was passed to \IPS\Task::queue()
	 * @param	int							$offset	Offset
	 * @return	int							New offset
	 * @throws	\IPS\Task\Queue\OutOfRangeException	Indicates offset doesn't exist and thus task is complete
	 */
	public function run( $data, $offset )
	{
		foreach( \IPS\Db::i()->select( '*', 'frontpage_fpages', array( 'fpage_type=?', 'bbcode' ) ) as $fpage )
		{
			$update = array(
				'fpage_content' => \IPS\Text\LegacyParser::parseStatic( $fpage['fpage_content'] ),
				'fpage_type'    => 'html'
			);
			
			\IPS\Db::i()->update( 'frontpage_fpages', $update, array( 'fpage_id=?', $fpage['fpage_id'] ) );
		}

		throw new \IPS\Task\Queue\OutOfRangeException;
	}
	
	/**
	 * Get Progress
	 *
	 * @param	mixed					$data	Data as it was passed to \IPS\Task::queue()
	 * @param	int						$offset	Offset
	 * @return	array( 'text' => 'Doing something...', 'complete' => 50 )	Text explaining task and percentage complete
	 * @throws	\OutOfRangeException	Indicates offset doesn't exist and thus task is complete
	 */
	public function getProgress( $data, $offset )
	{
		return array( 'text' => \IPS\Member::loggedIn()->language()->addToStack( 'converting_fpages_34x' ), 'complete' => 100 );
	}	
}